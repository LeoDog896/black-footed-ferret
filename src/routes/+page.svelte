<h1>the Black-Footed Ferret</h1>
<ul>
    <li>American polecat, prairie dog hunter, Mustela nigripes</li>
    <li>Is a flagship species, and many other species depend on it.</li>
    <li>Is an indicator species for the health of the grasslands and its nearby prarie dog population</li>
    <li>They were declared extinct in 1974, rediscovered in 1981, now there is very little genetic diversity</li>
    <li>Elizabeth Ann is a cloned black-footed ferret as a trailblazer for cloning for conservation efforts by Revive & Restore to mitigate inbreeding (and thus inbreeding depression)</li>
</ul>
